"""Calculate and print the area of a rectangle."""

# Define the dimensions of the rectangle
length = 10
width = 5

# Compute the area using the formula area = length * width
area = length * width

# If the dimensions change, this print statement will still show the correct result
print(f"Area of the rectangle: {area}")  # output the computed area

